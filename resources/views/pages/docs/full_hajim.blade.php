    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        3.4.1.&nbsp;Компания томонидан Инвесторнинг мазкур шартнома шартларига риоя қилиниши юзасидан мониторинг
        ўтказилганида унда иштирок этиш ва мониторинг предметига оид масалалар бўйича тушунтиришлар бериш;</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        3.4.2.&nbsp;Ўзбекистон Республикасининг амалдаги қонунчилик ҳужжатларига мувофиқ Компаниядан олиниши мумкин
        бўлган маълумотларни сўраб олиш.</p>
    <p style="margin-top:6pt; margin-bottom:0pt; text-align:center; line-height:normal;"><a
            name="_465177"></a><strong>IV. Шартнома мажбуриятларини бажариш мониторинги</strong></p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        4.1.&nbsp;Инвестор томонидан Йиғим тўланишини мониторинг қилиш Компания томонидан Жамғарма фаолиятига тегишли
        банк ва бухгалтерия ҳужжатлари асосида амалга оширилади.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        4.2.&nbsp;Йиғим тўлашда камчиликлар аниқланган тақдирда Компания Инвесторга аниқланган камчиликларни ўз вақтида
        ва зудлик билан бартараф этиш юзасидан тегишли талабнома юборади ҳамда ушбу талабнома Инвестор томонидан 3 (уч)
        иш кунидан кечиктирмасдан қаноатлантирилиши лозим.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        4.3.&nbsp;Инвестор томонидан Йиғим тўлаш муддати бузилганлиги Компания томонидан тузилган тегишли далолатнома
        билан тасдиқланади.</p>
    <p style="margin-top:6pt; margin-bottom:0pt; text-align:center; line-height:normal;"><strong>V. Шартномани
            ўзгартириш ва муддатидан олдин бекор қилиш тартиби</strong></p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.1.&nbsp;Мазкур шартнома Тарафлар келишувига кўра ўзгартирилиши мумкин.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.2.&nbsp;Мазкур шартномага ҳар қандай ўзгартиришлар ва қўшимчалар Тарафларнинг келишуви асосида Тарафларнинг
        ваколатли вакиллари имзолаган қўшимча келишувни имзолаш йўли билан киритилади.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.3.&nbsp;Компания мазкур шартномани қуйидаги ҳолатлар юз берган тақдирда<br>ўз ташаббуси билан Йиғимнинг
        тўланган қисми юзасидан Инвесторга компенсация тўламасдан муддатидан олдин бир тарафлама бекор қилиш ҳуқуқига
        эга:</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.3.1.&nbsp;мазкур шартномага иловада келтирилган режа-график муддатлари<br>10 (ўн) иш кунидан ортиқ муддатга
        бузилганда;</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.3.2.&nbsp;мазкур шартноманинг 8.1-бандида баён этилган ҳодисалар узлуксиз 6 (олти) ойдан ортиқ муддатга
        чўзилиб кетганда ва бу давом этганда;</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.3.3.&nbsp;Инвестор тугатилган ёки тўловга қобилиятсиз деб топилганда;</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.3.4.&nbsp;Инвестор томонидан ўз мажбуриятлари бажарилмаганида;</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        5.3.5.&nbsp;мазкур шартномани имзолаш жараёнида ишончсиз ва/ёки ёлғон маълумотли ҳужжатларни Компанияга тақдим
        этган ҳолати аниқланганида.</p>
    <p style="margin-top:6pt; margin-bottom:0pt; text-align:center; line-height:normal;"><strong>VI. Тарафларнинг
            жавобгарлиги</strong></p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:27pt; text-align:justify; line-height:normal;">
        6.1.&nbsp;Инвестор Компанияга қуйидаги ҳолатларда ва миқдорларда пеня тўлайди:</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:27pt; text-align:justify; line-height:normal;">
        &ndash;&nbsp;Йиғим тўлови муддатларини бузганлик учун кечктирилган ҳар бир кун учун тўлови кечиктирилган Йиғим
        суммасининг 0,01&nbsp;%и миқдорида, бироқ, тўлови кечиктирилган Йиғим суммасининг 15&nbsp;%идан ортиқ бўлмаган
        миқдорда;</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:27pt; text-align:justify; line-height:normal;">
        &ndash;&nbsp;Мазкур шартномада кўрсатилган ҳар қандай параметрлар ўзгарганда, улар ўзгарганлиги аниқланган
        (тасдиқланган) санадан бошлаб 5 (беш) иш куни мобайнида мазкур шартномага Йиғим миқдорини аниқлаштириш юзасидан
        тегишли ўзгартиришлар киритиш учун мазкур шартномага ўзгартиришлар киритиши тўғрисидаги қўшимча келишув
        лойиҳасини илова қилгани ҳолда Компанияга ёзма равишда мурожаат қилмаганда ҳар бир кечиккан кун учун Йиғим
        суммасининг 0,01%и миқдорида, бироқ, тўлови кечиктирилган Йиғим суммасининг 15%идан ортиқ бўлмаган миқдорда.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        6.2.&nbsp;Жарима санкцияларининг қўлланилиши Инвесторни мазкур шартномада белгиланган мажбуриятларни бажаришдан
        озод қилмайди.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        6.3.&nbsp;Инвестор қуйидаги ҳатти-ҳаракатларни содир этганида Компания мазкур шартномани бекор қилиш ҳақидаги
        талаб билан судга мурожаат қилади:</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        &mdash;&nbsp;Йиғим тўлаш муддатларини белгиланган муддатдан уч ойдан ошиқ муддатга кечиктирганда;</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        &mdash;&nbsp;Инвестор ўз ихтиёри билан тугатилганида ёки банкротликка учрганида,<br>шу жумладан, қасддан шундай
        ҳаракатлар амалга оширилиши туфайли Йиғим тўлашни дастлабки шартларда амалга ошириш имконияти мавжуд
        бўлмаслигига олиб келганида.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        6.4.&nbsp;Инвестор томонидан мажбуриятлари бажарилмаслиги ёки у томонидан бошқа ҳаракатлар содир этилганлиги
        туфайли мазкур шартномани бекор қилиш пайтида Инвестор томонидан етказилган моддий зарар мустақил аудитор ва/ёки
        мустақил баҳоловчи томонидан тақдим этилган ҳисоб-китобларга асосан баҳоланади ва суд тартибида ундан
        ундирилади.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        6.5.&nbsp;Мазкур шартноманинг амал қилиш муддати тугаши унинг бузилганлиги учун жавобгарликнинг тугатилишига
        олиб келмайди.</p>
    <p style="margin-top:6pt; margin-bottom:0pt; text-align:center; line-height:normal;"><strong>VII. Манфаатлар
            тўқнашуви</strong></p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        7.1.&nbsp;Компания ва Инвестор ходимлари ўз хизмат вазифаларини бажаришда манфаатлар тўқнашувига сабаб бўладиган
        шахсий манфаатдорлик ҳолатларига йўл қўймасликлари керак.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        7.2.&nbsp;Манфаатлар тўқнашуви Компания ва Инвестор ходимларининг шахсий манфаатлари уларнинг ўз хизмат
        вазифаларини холисона ва беғараз бажаришига таъсир кўрсатадиган ёки таъсир кўрсатиши мумкин бўлган ҳолатларда
        пайдо бўлади.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        7.3.&nbsp;Компания ва Инвестор ходимларининг шахсий манфаатдорлиги уларнинг шахсан ўзи ёки яқин қариндошлари,
        шунингдек улар яқин ёки ишбилармонлик муносабатларида бўладиган бошқа шахслар учун ҳар қандай наф кўриш ёки
        афзалликларга эга бўлишни<br>ўз ичига олади.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        7.4.&nbsp;Манфаатлар тўқнашуви юзага келган тақдирда Компания ва Инвестор ходимлари ўз раҳбарини бу ҳақида
        дарҳол хабардор қилиши керак.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:28.35pt; text-align:justify; line-height:normal;">
        7.5.&nbsp;Манфаатлар тўқнашуви мавжудлиги тўғрисидаги маълумотни олган Тараф уни тартибга келтириш бўйича
        иккинчи Тараф билан ҳамкорликда ўз вақтида чоралар кўриши шарт.</p>
    <p style="margin-top:6pt; margin-bottom:0pt; text-align:center; line-height:normal;"><strong>VIII.
            Форс-мажор</strong></p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        8.1.&nbsp;Агар Тарафларнинг ўз мажбуриятларини бажармаслик ёки лозим даражада бажармаслик мазкур шартнома
        имзоланганидан сўнг Тарафлар олдиндан кўра билиши<br>ёки бартараф қилиши мумкин бўлмаган фавқулодда вазиятлар,
        шу жумладан, ҳарбий ҳаракатлар, эпидемиялар, блокадалар, эмбарголар, шунингдек, зилзила, сув тошқини, ёнғинлар
        ва бошқа табиий офатлар натижасида енгилмас куч таъсиридаги ҳолатлар билан боғлиқ бўлса, мазкур шартнома
        Тарафлари мазкур шартнома бўйича ўз мажбуриятларини бажармаганликлари ёки лозим даражада бажармаганликлари учун
        жавобгарликдан озод этиладилар.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        8.2.&nbsp;Енгилмас куч таъсиридаги ҳолатлар юзага келган тақдирда Тараф ушбу ҳолатлар юзага келган пайтдан
        бошлаб 3 (уч) иш кунидан кечикмасдан бошқа Тарафни бу ҳақида хабардор қилишга мажбур. Бундай хабарда енгилмас
        куч таъсиридаги ҳолатлар характери ҳақидаги маълумотлар, шунингдек, уларнинг юзага келганини тасдиқловчи
        ҳужжатлар бўлиши лозим.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        8.3.&nbsp;Тараф енгилмас куч таъсиридаги ҳолатлар юзага келгани ҳақидаги хабарни олган пайтдан бошлаб Тарафлар
        30 (ўттиз) иш куни давомида енгилмас куч таъсиридаги ҳолатларнинг таъсирини камайтириш учун ўзаро ҳамкорликнинг
        энг самарали тартибини, шунингдек, мазкур шартнома шартларини ўзгартиришнинг энг мақбул усулларини белгилаб
        олишлари лозим.</p>
    <p style="margin-top:6pt; margin-bottom:0pt; text-align:center; line-height:normal;"><strong>IX. Низоларни ҳал
            қилиш
            тартиби</strong></p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        9.1.&nbsp;Мазкур шартномани бажариш давомида юзага келадиган ҳар қандай низолар<br>ва келишмовчиликлар Тарафлар
        ўртасида музокаралар йўли билан ҳал этилади.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        9.2.&nbsp;Музокаралар орқали келишувга эришилмаган тақдирда манфаатдор Тараф иккинчи тарафга ёзма шаклда
        талабнома хат юборади.</p>
    <p style="margin-top:0pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        9.3.&nbsp;Низолар Тарафлардан бирининг ёзма шаклдаги талабномаси юборилган санадан бошлаб 30 (ўттиз) иш куни
        давомида ҳал қилинмаган тақдирда ушбу низо Ўзбекистон Республикасининг амалдаги қонун ҳужжатларига мувофиқ суд
        тартибида ҳал этилади.</p>
    <p style="margin-top:6pt; margin-bottom:0pt; text-align:center; line-height:normal;"><strong>XI. Алоҳида
            шартлар</strong></p>
    <p style="margin-top:0.75pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        10.1.&nbsp;Бир Тарафдан бошқа Тарафга берилган ахборотнинг махфийлик шартлари<br>ҳар қандай Тарафнинг ёзма
        талабига биноан белгиланиши мумкин. Махфий деб топилган<br>ҳар қандай ахборот ошкор қилинмаслиги лозим.</p>
    <p style="margin-top:0.75pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        10.2.&nbsp;Махфийлик шартлари қуйидаги ҳолатлар учун қўлланилмайди:</p>
    <p style="margin-top:0.75pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        10.2.1.&nbsp;мазкур шартномани амалга ошириш мақсадида мазкур шартнома ва унинг шартлари тўғрисидаги ахборотни
        давлат органларига тақдим этишда;</p>
    <p style="margin-top:0.75pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        10.2.2.&nbsp;бундай ахборот Ўзбекистон Республикасининг амалдаги қонун ҳужжатларига мувофиқ ёки Ўзбекистон
        Республикасининг давлат органлари ёки бошқа ваколатли органлари томонидан қонуний даъво тақдим этилганда фақат
        тушган сўров ҳажмида ошкор этилиши керак бўлганида.</p>
    <p style="margin-top:0.75pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        10.3.&nbsp;Почта реквизитлари ўзгарганда Тарафлар бир-бирларини дарҳол хабардор қилишлари керак. Эски манзиллар
        бўйича амалга оширилган ҳатти-ҳаракатлар, уларнинг ўзгариши ҳақида хабарнома олинмагунча қабул қилинган
        ҳисобланади.</p>
    <p style="margin-top:0.75pt; margin-bottom:0pt; text-indent:35.45pt; text-align:justify; line-height:normal;">
        10.4.&nbsp;Мазкур шартнома шартлари ҳуқуқий оқибатларга олиб келиши ва ҳуқуқий ворислар, бенефициарлар ва
        Тарафлар томонидан тайинланган шахсларнинг келажакдаги барча кўринишлари ёки шакллари учун мажбурий бўлиши
        керак. Бундан ташқари, мазкур шартнома бўйича Тарафларнинг ҳуқуқ ва мажбуриятлари хўжалик жамиятларининг бошқа
        ташкилотлар билан бирлашуви, консолидация, қўшиб юбориш ёки Инвестор бошқарувида бошқача эгалик қилиш ёки
        ўзгартириш орқали бузилмаслиги керак.</p>
